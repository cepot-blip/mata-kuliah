# Operator Aritmatika 
x=20
y=15
print("===============================")
print("Nilai x = ", x)
print("Nilai y = ", y)
print("===============================")
print("Hasil Operator Penjumlahan")
print("===============================")
print(" X +  Y   : ",x+y)
print(" X -  Y   : ",x-y)
print(" X *  Y   : ",x*y)
print(" X /  Y   : ",x/y)
print(" X // Y   : ",x//y)
print(" X %  Y   : ",x%y)
print(" X ** Y   : ",x**y)


#Operator Perbandingan
